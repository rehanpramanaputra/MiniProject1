# Mendefinisikan array 2 dimensi
array_2d = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Mengakses elemen array
print("Elemen array pada indeks (0, 0):", array_2d[0][0])  # Output: 1
print("Elemen array pada indeks (1, 2):", array_2d[1][2])  # Output: 6

# Menggunakan loop untuk melakukan iterasi dan mencetak setiap elemen
print("Isi array 2 dimensi:")
for row in array_2d:
    for element in row:
        print(element, end=" ")
    print()  # Baris baru setiap selesai mencetak satu baris array 2 dimensi





















